'''
    彩票抽奖1
    1.福彩是以3位自然数为投注号码的彩票,投注者从000-999的数字中选择一个3位数进行投注.
    2.福彩每注2元,每天17：45开奖,实现输出5张福彩。
    3.输出结果如下：
        福彩3D彩票
        ==========
        481
        077
        466
        826
        415
    4.知识回顾：
        random.choice()：指定序列中返回一个随机的元素。
                zfill()：返回指定长度的字符串，原字符串右对齐，前面填充0。
'''
import random

print("福彩3D彩票")
print("==========")
for i in range(5):
    print(str(random.choice(range(1, 999))).zfill(3))
